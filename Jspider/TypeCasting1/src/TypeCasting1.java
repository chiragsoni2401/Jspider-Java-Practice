
public class TypeCasting1 {

	public static void main(String[] args) {
		
		char c1= 'a';
		char c2= 'A';
		char c3= 'z';
		char c4= 'Z';
		//widening casting is implicit
		int n1= c1;
		int n2= c2;
		int n3= c3;
		int n4= c4;
		
		System.out.println("c1"+"-->"+n1);
		System.out.println("c2"+"-->"+n2);
		System.out.println("c3"+"-->"+n3);
		System.out.println("c4"+"-->"+n4);



		

	}

}
